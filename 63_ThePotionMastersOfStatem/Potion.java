public enum Potion {
    WATER("water"),
    ELIXIR("elixir"),
    POISON_POTION("poison potion"),
    FLYING_POTION("flying potion"),
    INVISIBILITY_POTION("invisibility potion"),
    NIGHT_SIGHT_POTION("night sight potion"),
    CLOUDY_BREW("cloudy brew"),
    WRAITH_POTION("wraith potion"),
    RUINED_POTION("ruined potion");

    public final String name;

    Potion(String name) {
        this.name = name;
    }
}


